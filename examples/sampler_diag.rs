//! Debug harness: dump first 20 sampler values for known seed.
#![allow(missing_docs)]
use rfalco::sampler::{sigma_min, ChaCha20Prng, SamplerContext};
fn main() {
    let seed: [u8; 56] = [
        0x99, 0xaa, 0x50, 0xc3, 0x7f, 0x60, 0xc1, 0x6d, 0xb9, 0x0e, 0xdb, 0x15, 0xda, 0xd7, 0x22,
        0xcc, 0x13, 0x20, 0x8e, 0x91, 0x3e, 0xd1, 0x1c, 0x11, 0x57, 0x5b, 0x9a, 0x18, 0x23, 0xa9,
        0x08, 0xef, 0xaa, 0x63, 0xfe, 0xee, 0xdd, 0x97, 0x0b, 0x47, 0x72, 0xe1, 0x24, 0x3a, 0x64,
        0x39, 0xa1, 0x78, 0x29, 0x63, 0xbd, 0xa0, 0xf3, 0x3e, 0x19, 0x25,
    ];
    let prng = ChaCha20Prng::from_seed_bytes(&seed);
    let mut ctx = SamplerContext {
        prng,
        sigma_min: sigma_min(9),
    };
    let mu = 7.0_f64 / 10.0_f64;
    let isigma = 100.0_f64 / 155.0_f64;
    print!("first 20:");
    for _ in 0..20 {
        print!(" {}", ctx.sample(mu, isigma));
    }
    println!();
}
